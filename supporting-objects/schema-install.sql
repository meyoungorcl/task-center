            when t.completed_date between trunc(sysdate, 'IW') and trunc(sysdate, 'IW') + 6
            then t.task_id
        end
    ) as tasks_completed_this_week,
    sum(case when st.is_done_yn = 'N' then 1 else 0 end) as open_tasks,
    sum(case when st.status_code = 'IN_PROGRESS' then 1 else 0 end) as in_progress_tasks,
    sum(case when st.status_code = 'IN_REVIEW' then 1 else 0 end) as in_review_tasks,
    sum(case when st.status_code = 'BLOCKED' then 1 else 0 end) as blocked_tasks,
    sum(case when st.is_done_yn = 'Y' then 1 else 0 end) as completed_tasks,
    sum(
        case
            when t.due_date < trunc(sysdate)
             and st.is_done_yn = 'N'
            then 1
            else 0
        end
    ) as overdue_tasks,
    listagg(
        case
            when t.due_date between trunc(sysdate) and trunc(sysdate) + 7
             and st.is_done_yn = 'N'
            then t.task_key || ' - ' || t.title || ' due ' || to_char(t.due_date, 'YYYY-MM-DD')
        end,
        chr(10)
    ) within group (order by t.due_date nulls last, t.task_key) as upcoming_due_tasks
from tsk_teams team
left join tsk_tasks t
    on t.team_id = team.team_id
left join tsk_statuses st
    on st.status_id = t.status_id
group by
    team.team_id,
    team.team_name;
/


