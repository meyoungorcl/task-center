  CREATE OR REPLACE EDITIONABLE TRIGGER "TSK_TASKS_BIU"
before insert or update on tsk_tasks
for each row
declare
    l_done_yn tsk_statuses.is_done_yn%type;
begin
    if inserting then
        :new.created_at := coalesce(:new.created_at, systimestamp);
        :new.created_by := coalesce(:new.created_by, tsk_app_user());

        if :new.status_id is null then
            select status_id
              into :new.status_id
              from tsk_statuses
             where status_code = 'NOT_STARTED';
        end if;
    end if;

    :new.updated_at := systimestamp;
    :new.updated_by := tsk_app_user();

    if :new.percent_complete is null then
        :new.percent_complete := 0;
    end if;

    if :new.status_id is not null then
        select is_done_yn
          into l_done_yn
          from tsk_statuses
         where status_id = :new.status_id;

        if l_done_yn = 'Y' then
            :new.percent_complete := 100;

            if :new.completed_date is null then
                :new.completed_date := trunc(sysdate);
            end if;
        elsif updating and nvl(:old.status_id, -1) <> nvl(:new.status_id, -1) then
            :new.completed_date := null;
        end if;
    end if;
end;

