  CREATE OR REPLACE FORCE EDITIONABLE VIEW "TSK_V_DAILY_MEMBER_EMAIL" ("TEAM_ID", "TEAM_NAME", "TEAM_MEMBER_ID", "DISPLAY_NAME", "EMAIL", "OPEN_TASKS", "IN_PROGRESS_TASKS", "IN_REVIEW_TASKS", "BLOCKED_TASKS", "COMPLETED_TASKS", "OVERDUE_TASKS", "DUE_TODAY_TASKS", "UPDATES_TODAY", "OPEN_TASK_SUMMARY") AS
  select
    tm.team_id,
    team.team_name,
    tm.team_member_id,
    u.display_name,
    u.email,
    count(distinct case when st.is_done_yn = 'N' then t.task_id end) as open_tasks,
    count(distinct case when st.status_code = 'IN_PROGRESS' then t.task_id end) as in_progress_tasks,
    count(distinct case when st.status_code = 'IN_REVIEW' then t.task_id end) as in_review_tasks,
    count(distinct case when st.status_code = 'BLOCKED' then t.task_id end) as blocked_tasks,
    count(distinct case when st.is_done_yn = 'Y' then t.task_id end) as completed_tasks,
    count(
        distinct case
            when t.due_date < trunc(sysdate)
             and st.is_done_yn = 'N'
            then t.task_id
        end
    ) as overdue_tasks,
    count(
        distinct case
            when t.due_date = trunc(sysdate)
             and st.is_done_yn = 'N'
            then t.task_id
        end
    ) as due_today_tasks,
    (
        select count(*)
        from tsk_task_updates upd
        join tsk_task_assignees ta2
            on ta2.task_id = upd.task_id
           and ta2.team_member_id = tm.team_member_id
           and ta2.active_yn = 'Y'
        where upd.created_at >= cast(trunc(sysdate) as timestamp)
          and upd.created_at <  cast(trunc(sysdate + 1) as timestamp)
    ) as updates_today,
    listagg(
        case
            when st.is_done_yn = 'N' then
                t.task_key || ' - ' || t.title || ' [' || st.status_name || '] due ' ||
                coalesce(to_char(t.due_date, 'YYYY-MM-DD'), 'No due date')
        end,
        chr(10)
    ) within group (order by t.due_date nulls last, t.task_key) as open_task_summary
from tsk_team_members tm
join tsk_teams team
    on team.team_id = tm.team_id
join tsk_users u
    on u.user_id = tm.user_id
left join tsk_task_assignees ta
    on ta.team_member_id = tm.team_member_id
   and ta.active_yn = 'Y'
left join tsk_tasks t
    on t.task_id = ta.task_id
left join tsk_statuses st
    on st.status_id = t.status_id
where tm.active_yn = 'Y'
  and u.active_yn = 'Y'
group by
    tm.team_id,
    team.team_name,
    tm.team_member_id,
    u.display_name,
    u.email;
