  CREATE OR REPLACE EDITIONABLE FUNCTION "TSK_APP_USER"
return varchar2
as
begin
    return coalesce(
        sys_context('APEX$SESSION', 'APP_USER'),
        sys_context('USERENV', 'SESSION_USER')
    );
end tsk_app_user;
