            '<div style="font-family:Arial,sans-serif;color:#172033;line-height:1.45">' ||

            '<h2 style="margin:0 0 4px">Monthly Team Progress</h2>' ||

            '<div style="color:#5f6b7a;margin-bottom:18px">' ||

