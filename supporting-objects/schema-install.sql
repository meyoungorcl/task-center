            log_change(to_char(:new.task_id), :new.task_id, 'UPDATE', 'PRIORITY_CODE',
                       :old.priority_code, :new.priority_code, null);
        end if;

        if nvl(:old.due_date, date '1900-01-01') <>
           nvl(:new.due_date, date '1900-01-01') then
            log_change(to_char(:new.task_id), :new.task_id, 'UPDATE', 'DUE_DATE',
                       to_char(:old.due_date, 'YYYY-MM-DD'),
                       to_char(:new.due_date, 'YYYY-MM-DD'),
                       null);
        end if;

