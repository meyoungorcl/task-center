            to_char(:old.task_id),
            :old.task_id,
            'DELETE',
            null,
            null,
            null,
            'Task deleted'
        );

    elsif updating then

        if nvl(:old.title, chr(0)) <> nvl(:new.title, chr(0)) then
            log_change(to_char(:new.task_id), :new.task_id, 'UPDATE', 'TITLE',
                       :old.title, :new.title, null);
        end if;

        if nvl(:old.type_id, -1) <> nvl(:new.type_id, -1) then
            log_change(to_char(:new.task_id), :new.task_id, 'UPDATE', 'TYPE_ID',
                       to_char(:old.type_id), to_char(:new.type_id), null);
        end if;

        if nvl(:old.status_id, -1) <> nvl(:new.status_id, -1) then
            log_change(to_char(:new.task_id), :new.task_id, 'UPDATE', 'STATUS_ID',
                       to_char(:old.status_id), to_char(:new.status_id), null);
        end if;

