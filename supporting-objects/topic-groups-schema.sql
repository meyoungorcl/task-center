declare
    l_count number;
begin
    select count(*)
      into l_count
      from user_tables
     where table_name = 'TSK_TOPIC_GROUPS';

    if l_count = 0 then
        execute immediate q'[
            create table tsk_topic_groups (
                topic_group_id number generated by default on null as identity
                    constraint tsk_topic_groups_nn01 not null,
                topic_name varchar2(200) not null,
                description varchar2(1000),
                active_yn varchar2(1) default 'Y' not null,
                created_at timestamp default systimestamp not null,
                created_by varchar2(255),
                updated_at timestamp default systimestamp not null,
                updated_by varchar2(255),
                constraint tsk_topic_groups_pk primary key (topic_group_id),
                constraint tsk_topic_groups_u01 unique (topic_name),
                constraint tsk_topic_groups_active_ck check (active_yn in ('Y','N'))
            )
        ]';
    end if;
end;
/

declare
    l_count number;
begin
    select count(*)
      into l_count
      from user_tab_columns
     where table_name = 'TSK_TASKS'
       and column_name = 'TOPIC_GROUP_ID';

    if l_count = 0 then
        execute immediate 'alter table tsk_tasks add topic_group_id number';
    end if;
end;
/

declare
    l_count number;
begin
    select count(*)
      into l_count
      from user_constraints
     where table_name = 'TSK_TASKS'
       and constraint_name = 'TSK_TASKS_TOPIC_GROUP_FK';

    if l_count = 0 then
        execute immediate q'[
            alter table tsk_tasks add constraint tsk_tasks_topic_group_fk
                foreign key (topic_group_id)
                references tsk_topic_groups (topic_group_id)
        ]';
    end if;
end;
/

declare
    l_count number;
begin
    select count(*)
      into l_count
      from user_indexes
     where index_name = 'TSK_TASKS_I06';

    if l_count = 0 then
        execute immediate 'create index tsk_tasks_i06 on tsk_tasks (topic_group_id)';
    end if;
end;
