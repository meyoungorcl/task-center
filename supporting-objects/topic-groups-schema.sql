insert into tsk_task_topic_groups (
    task_id,
    topic_group_id
)
select t.task_id,
       t.topic_group_id
  from tsk_tasks t
 where t.topic_group_id is not null
   and not exists (
           select 1
             from tsk_task_topic_groups ttg
            where ttg.task_id = t.task_id
              and ttg.topic_group_id = t.topic_group_id
       );
