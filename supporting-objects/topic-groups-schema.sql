create or replace trigger tsk_topic_groups_biu
before insert or update on tsk_topic_groups
for each row
begin
    if inserting then
        :new.created_at := coalesce(:new.created_at, systimestamp);
        :new.created_by := coalesce(:new.created_by, tsk_app_user());
    end if;

    :new.updated_at := systimestamp;
    :new.updated_by := tsk_app_user();
end;
/

alter trigger tsk_topic_groups_biu enable
/

